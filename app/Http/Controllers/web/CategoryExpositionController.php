<?php

namespace App\Http\Controllers\web;

use App\Models\CategoryExposition;
use Illuminate\Http\Request;
use App\Http\Controllers\Controller;

class CategoryExpositionController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $items = CategoryExposition::all();
        return view('Admin.Exposition.Cat.index', compact('items'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('Admin.Exposition.Cat.edit');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $Cat = new CategoryExposition($request->all());
        $Cat->save();
        return redirect()->route('CatExposition.index')
            ->with('success', 'news created successfully.');
    }

    /**
     * Display the specified resource.
     *
     * @param  int $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $item = CategoryExposition::findOrFail($id);
        return view('Admin.Exposition.Cat.edit', compact('item'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request $request
     * @param  int $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        CategoryExposition::destroy($id);
        return redirect()->route('CatExposition.index')
            ->with('field','Category deleted successfully');
    }
}
